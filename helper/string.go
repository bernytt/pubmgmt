var (
	alphaNumericRegex = regexp.MustCompile(alphaNumericRegexString)
	emailRegex        = regexp.MustCompile(emailRegexString)
)

func IsEmail(str string) bool  { return emailRegex.MatchString(str) }
func IsString(str string) bool { return alphaNumericRegex.MatchString(str) }

func Contains(str []string, substring string) bool {
	for _, s := range str {
		if s == substring {
			return true
		}
	}
	return false
}

func HasSuffix(str []string, substring string) bool {
	for _, s := range str {
		if strings.HasSuffix(substring, s) {
			return true
		}
	}
	return false
}
